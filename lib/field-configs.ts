export function getEditableFields(table: TableKey): FieldConfig[] {
  return TABLE_FIELDS[table].filter((field) => !field.autoGenerated);
}

export function getDefaultFormData(table: TableKey): Record<string, unknown> {
  const data: Record<string, unknown> = {};

  for (const field of getEditableFields(table)) {
    if (field.defaultValue !== undefined) {
      data[field.key] = field.defaultValue;
    } else if (field.type === "number") {
      data[field.key] = "";
    } else {
      data[field.key] = "";
    }
  }

  return data;
}

export function preparePayload(
  table: TableKey,
  formData: Record<string, unknown>,
  mode: "create" | "edit"
): Record<string, unknown> {
  const fields = getEditableFields(table);
  const payload: Record<string, unknown> = {};

  for (const field of fields) {
    const value = formData[field.key];

    if (value === "" || value === undefined || value === null) {
      if (mode === "create" && field.defaultValue !== undefined) {
        payload[field.key] = field.defaultValue;
      }
      continue;
    }

    if (field.type === "number") {
      const num = Number(value);
      if (!Number.isNaN(num)) {
        payload[field.key] = num;
      }
      continue;
    }

    if (field.type === "json" || field.type === "images") {
      if (typeof value === "string") {
        try {
          payload[field.key] = JSON.parse(value);
        } catch {
          payload[field.key] = value;
        }
      } else {
        payload[field.key] = value;
      }
      continue;
    }

    payload[field.key] = value;
  }

  return payload;
}

export function getRecordLabel(
  table: TableKey,
  record: Record<string, unknown>
): string {
  const nameKey =
    table === "places" || table === "events" ? "title" : "name";
  const value = record[nameKey];
  return value ? String(value) : `Record #${record.id ?? "?"}`;
}
